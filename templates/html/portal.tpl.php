<h2>das Portal:</h2>
<p>Dieses Portal ist praktisch die zentrale Stelle des Oldenburger Netzwerkes an der alle Informationen gebündelt werden.<br>
Jeder Benutzer hat hier einen Account über den er sich Adressen für seine Nodes und Clients reservieren kann.<br></p>

<p>Bist du neu, legst du unter Registrieren einen Benutzeracount an und trägst dann einen neuen Client (Laptop/PC) oder einen neuen Node (Router) ein und bekommst einen VPN-Key und eine Netzwerkadresse zugeweisen. Mit diesen Zwei Sachen kommst du dann ins Netz.</p>

<p>Bist du schon länger dabei, kannst du über diese Seite das Netzwerk beobachten und bekommst aktuelle Informationen zu Problemen.</p>

